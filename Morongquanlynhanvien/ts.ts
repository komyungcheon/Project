
export class Employee {
    private name :string;
    private gender : string;
    private birthday : string;
    private  email : string;
    private phone: string;


    constructor(name: string, gender: string, birthday: string, email: string, phone: string) {
        this.name = name;
        this.gender = gender;
        this.birthday = birthday;
        this.email = email;
        this.phone = phone;

    }

}